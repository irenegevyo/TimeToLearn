package com.gevyo.android.timetolearn;

public class MovieData {
    String name;
    String image_url;
    String year;
    String director;

    MovieData (String name, String image_url, String year, String director) {
        this.name = name;
        this.image_url = image_url;
        this.year = year;
        this.director = director;
    }
}
